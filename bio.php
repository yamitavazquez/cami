                En 2010 viajó. Comprobó que el mapa y el territorio no se parecen. Senegal se abrió a través de lxs maestrxs que la formaron, pero sobre todo su mirada fue la que transmutó y se tradujo en danza, desde otro centro de gravedad compartido transoceánicamente.
